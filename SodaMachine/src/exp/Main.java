/**
     * Method that starts the soda machine
     */
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        for (int i = 0; i < s.length; i++) {
            System.out.println(s[i].getSystemPortName());

        }
        s[1].openPort();
        s[1].setBaudRate(9600);
        //background thread
        Thread back;
        back = new Thread() {
            public void run() {
                try {
                    //infinite loop
                    while (true) {
                        //wait for data
                        while (s[1].bytesAvailable() == 0) {
                            Thread.sleep(20);
                        }

                        byte[] readBuffer = new byte[s[1].bytesAvailable()];
                        s[1].readBytes(readBuffer, readBuffer.length);
                        //user pressed Tiva C sensor for $1
                        if (readBuffer[0] == '1') {
                            if (c + 1 > 9) {
                                String s1 = "C1";
                                byte[] b = s1.getBytes(StandardCharsets.US_ASCII);
                                s[1].writeBytes(b, b.length);
                            } else {

                                c++;
                                Mony.setText(c + "");
                            }

                        }
                        //user pressed Tiva C sensor for $2
                        else if (readBuffer[0] == '2') {
                            if (c + 2 > 9) {
                                String s1 = "C2";
                                byte[] b = s1.getBytes(StandardCharsets.US_ASCII);
                                s[1].writeBytes(b, b.length);

                            } else {
                                c = c + 2;
                                Mony.setText(c + "");
                            }
                        }
                        //print received data
                        //System.out.println(new String(readBuffer));
                    }
                } catch (InterruptedException e) {
                    //something went wrong
                    e.printStackTrace();
                    s[1].closePort();
                }
            }

        };
        //start  money process
        back.start();
        //thread used to control available sodas
        Thread moni = new Thread() {
            public void run() {
                try {
                    can.setEnabled(true);
                    while (true) {
                        if (c > 5 && xc > 0) {
                            r1.setEnabled(true);
                        } else {
                            r1.setEnabled(false);
                        }
                        if (c > 6 && yc > 0) {
                            r2.setEnabled(true);
                        } else {
                            r2.setEnabled(false);
                        }
                        if (c > 7 && zc > 0) {
                            r3.setEnabled(true);
                        } else {

                            r3.setEnabled(false);

                        }
                        xco.setText(xc + "");
                        yco.setText(yc + "");
                        zco.setText(zc + "");
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                    s[1].closePort();
                }
            }

        };
        //start  money validation
        moni.start();
        //read las values of inventory in soda machine
        try {
            Scanner con = new Scanner(new File("C:\\Users\\da supa pc\\Documents\\NetBeansProjects\\Exp\\container.txt"));
            xc = Integer.parseInt(con.nextLine());
            yc = Integer.parseInt(con.nextLine());
            zc = Integer.parseInt(con.nextLine());
            mony_ventas = Integer.parseInt(con.nextLine());
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

        jButton5.setVisible(false);
    }//GEN-LAST:event_jButton5ActionPerformed
