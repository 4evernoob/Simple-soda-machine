    private void rewriteContainers() {
        try {
            FileWriter containerRestore = new FileWriter("container.txt");
            containerRestore.write(xc + "\r\n");
            containerRestore.write(yc + "\r\n");
            containerRestore.write(zc + "\r\n");
            containerRestore.write(mony_ventas + "\r\n");
            containerRestore.close();
        } catch (IOException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    /**
     * Method that calculates the change that the machine will give you
     */
    private void giveChange(int value) {
        String s1 = "C" + (c - value);
        byte[] b = s1.getBytes(StandardCharsets.US_ASCII);
        s[1].writeBytes(b, b.length);

        c = 0;
        Mony.setText(c + "");
    }

    /**
     * Method that activates the soda you bought
     */
    private void giveProd(int ch, int val) {
        String s1 = "R" + ch + "" + (c - val);
        byte[] b = s1.getBytes(StandardCharsets.US_ASCII);
        s[1].writeBytes(b, b.length);

        c = 0;
        Mony.setText(c + "");

    }

